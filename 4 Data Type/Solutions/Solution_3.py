# Type conversion
num = input("Enter A number: ")
int_num = int(num)
float_num = float(num)
string_num = str(num)  # optional By default it will be string
print(num, " Converted to int ", type(int_num))
print(num, " Converted to float  ", type(float_num))
print(num, " Converted to string ", type(string_num))  # defalut String
