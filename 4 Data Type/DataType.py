# Integer (int)
print("Integer")
a = 10
b = -5
print(a, b)

# Float (float)
print("\nFloat")
x = 3.14
y = -2.5
print(x, y)

# String (str)
print("\nString")
name = "Sourabh"
msg = "Hello"
print(name, msg)

# Boolean (bool)
print("\nBoolean")
is_passed = True
is_failed = False
print(is_failed, is_passed)

print("\nNone")
H = None
print(H)


